function getCurrentBrowserVersion() {
 const ua = navigator.userAgent

 if (ua.includes('Chrome')) {
  let version = navigator.userAgent
   .match(/Chrome\/(\d+)/)

  return {
   name: 'Chrome',
   version: version ? version[1] : 'UNKNOW'
  }
 }
 if (ua.includes('Safari')) {
  let version = navigator.userAgent.match(/Version\/(\d+)/)
  return {
   name: 'Safari',
   version: version ? version[1] : 'UNKNOW'
  }
 }
 if (ua.includes('Firefox')) {
  let version = navigator.userAgent.match(/Firefox\/(\d+)/)
  return {
   name: 'Firefox',
   version: version ? version[1] : 'UNKNOW'
  }
 }
}


